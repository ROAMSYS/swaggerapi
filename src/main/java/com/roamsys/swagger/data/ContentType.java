package com.roamsys.swagger.data;

/**
 * List of MIME content type constants
 * @author mbartel
 */
public class ContentType {
    /**
     * The standard content type for JSON UTF-8 encoded responses
     */
    final public static String JSON_UTF8 = "application/json;charset=UTF-8";
    /**
     * Content type for plain text UTF-8 encoded
     */
    final public static String TEXT = "text/plain";
    /**
     * Content type for RSS feeds
     */
    final public static String RSS = "application/rss+xml";
    /**
     * Content type for XML content
     */
    final public static String XML = "application/xml";
    /**
     * Content type for PDFs
     */
    final public static String PDF = "application/pdf";
    /**
     * Content type for ZIP archives
     */
    final public static String ZIP = "application/zip";
    /**
     * Content type for comma separated values
     */
    final public static String CSV = "text/csv";
    /**
     * Content type for HTML content
     */
    final public static String HTML = "text/html";
    /**
     * Content type for cascading style sheets
     */
    final public static String CSS = "text/css";
}
